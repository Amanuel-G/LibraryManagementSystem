    private void SearchItemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchItemKeyReleased
        try
        {
            String searchItem = SearchItem.getText();
            
            if (NameSearch.isSelected())
            {
                ResultSet rs = admin.seachLibrarianName(searchItem);
                LibrarianTable.setModel(DbUtils.resultSetToTableModel(rs));
            }
            
            else if (IDSearch.isSelected())
            {
                ResultSet rs = admin.seachLibrarianID(searchItem);
                LibrarianTable.setModel(DbUtils.resultSetToTableModel(rs));
            }
                        
            
            else if (EmailSearch.isSelected())
            {
                ResultSet rs = admin.seachLibrarianEmail(searchItem);
                LibrarianTable.setModel(DbUtils.resultSetToTableModel(rs));
            }
            
        }
        
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SearchItemKeyReleased

