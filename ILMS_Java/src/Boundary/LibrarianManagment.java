    private void LibrarianTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LibrarianTableMouseClicked
        try
        {
            int row = LibrarianTable.getSelectedRow();
            
            if (row == -1) {}
            else
            {
                String ID = (LibrarianTable.getModel().getValueAt(row, 0)).toString();  

                JDBC conn = new JDBC();
                Librarian librarian = conn.returnLibrarian(ID);

                loadImage(librarian.getID());

                LibrarianID.setText(librarian.getID());
                LibrarianName.setText(librarian.getName());
                Email.setText(librarian.getEmail());
            
            }
        }
        
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_LibrarianTableMouseClicked

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        
        File file = chooser.getSelectedFile();
        String filename = file.getAbsolutePath();
        
        try
        {
            File image = new File(filename);
            FileInputStream stream = new FileInputStream(image);
            
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            
            byte[] buf = new byte[1024];
            
            for (int readNum; (readNum = stream.read(buf)) != -1;)
            {
                bos.write(buf, 0, readNum);
            }
            
            imageData = bos.toByteArray();
            picture = new ImageIcon(imageData);
            
            LibrarianImage.setText("");
            LibrarianImage.setIcon(picture);
            
        }
        
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_jButton9ActionPerformed

